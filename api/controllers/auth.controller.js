import User from '../models/user.model.js';
import bycrypt from 'bcryptjs';
import { errorHandler } from '../utils/errors.js';
import jwt from 'jsonwebtoken';


export const signup =  async (req, res, next) => {
    // destructure, hash and save data in database
   const {username, email, password } = req.body;
   const hashedPassword =  bycrypt.hashSync(password, 10);  
   const newUser = new User({ username, email, password: hashedPassword });
   try {
    await newUser.save()
    res.status(201).json("Success(201): user created in database");
   } catch (error) {
    next(error);
   }    
}

export const signin = async (req, res, next) => {    
  const {email, password} = req.body;
  try {
    // you can authenticate the user using username + password
    // or email + password as both will be unique
    const validUser = await User.findOne({ email });
    if(!validUser) return next(errorHandler(404, "User not found!"));
    //compare password passed in from user by hashing it first with compareSync   
    const validPassword = bycrypt.compareSync(password, validUser.password);
    if(!validPassword) return next(errorHandler(404, "Email, or password incorrect!"));
    // user is validated so create json web token cookie adding our own salt from process.env
    const token = jwt.sign({id: validUser._id}, process.env.JWT_SECRET);
    // remove the hashed password from the res
    const { password: pass, ...rest} = validUser._doc;
    res.cookie('access_token', token, { httpOnly: true }).status(200).json(rest);
  } catch (error) {
    next(error);
    
  }  
}

export const google = async (req, res, next) => {
  try {
    // check if user with that email exists in our DB
    const user = await User.findOne({email: req.body.email});
    console.log("User.findOne: " +user)
    if(user) {
      // user is in db so create json web token cookie adding our own salt from process.env
      const token = jwt.sign({id: user._id}, process.env.JWT_SECRET);
      // seperate the password  using ...rest so we don't send it      
      const { password: pass, ...rest} = user._doc;
      res.cookie('access_token', token, { httpOnly: true }).status(200).json(rest);
    } else {
      // since using google oauth we have no password to use in our database so
      // create one by getting last 8 from numbers and letter generated by .toString(36)
      const generatePassword = Math.random().toString(36).slice(-8) + Math.random().toString(36).slice(-8);
      const hashedPassword = bycrypt.hashSync(generatePassword, 10);
      // create new user using our model
      const newUser = new User({
        username: req.body.name.split(" ").join("").toLowerCase() + Math.random().toString(36).slice(-4),
        email: req.body.email,
        password: hashedPassword,
        avatar: req.body.photo
      });
      // save new user
      await newUser.save();
      const token = jwt.sign({id: newUser._id}, process.env.JWT_SECRET);
      const { password: pass, ...rest} = newUser._doc;
      res.cookie('access_token', token, { httpOnly: true }).status(200).json(rest);
    }
  } catch (error) {
    next(error);
  }
}
